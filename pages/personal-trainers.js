import Head from 'next/head'
import React from 'react'
import { useEffect } from 'react'
import LoadingBar from '../components.js/LoadingBar';
import Trainers from '../components.js/Trainers';
function showAnimation(){
  let T1Btn = document.getElementById("t1-btn");
  let T2Btn = document.getElementById("t2-btn");
  let T3Btn = document.getElementById("t3-btn");
    if(document.documentElement.scrollTop + window.innerHeight  >= T1Btn.offsetTop){
      T1Btn.classList.add("fade-animation")
    }
    if(document.documentElement.scrollTop + window.innerHeight  >= T2Btn.offsetTop){
      T2Btn.classList.add("fade-animation")
    }
    if(document.documentElement.scrollTop + window.innerHeight  >= T3Btn.offsetTop){
      T3Btn.classList.add("fade-animation")
    }
    window.addEventListener("scroll",()=>{
    if(document.documentElement.scrollTop + window.innerHeight  >= T1Btn.offsetTop){
      T1Btn.classList.add("fade-animation")
    }
    if(document.documentElement.scrollTop + window.innerHeight  >= T2Btn.offsetTop){
      T2Btn.classList.add("fade-animation")
    }
    if(document.documentElement.scrollTop + window.innerHeight  >= T3Btn.offsetTop){
      T3Btn.classList.add("fade-animation")
    }
  })
}
export default function PersonalTrainers() {
  useEffect(() => {
    showAnimation()
   }, [])
    return (
        <>
        <Head>
           <title>Personal Trainers | Fithub</title>
           <meta name="description" content="Generated by create next app" />
           <link rel="icon" href={`${process.env.NEXT_PUBLIC_IMAGESPATH}/favicon.ico`} />
         </Head>
         <LoadingBar/>
    <Trainers/>
       </>
    )
  }